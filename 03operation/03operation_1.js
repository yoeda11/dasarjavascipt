// Penugasan JavaScript
// Operator Penugasan (=) menetapkan nilai ke variabel:
// Contoh Tugas / Penugasan
let xyz = 10; // Tetapkan nilai 10 ke variabel xyz
console.log(xyz); // Output: 10

let x = 5; // Tetapkan nilai 5 ke x
let y = 2; // Tetapkan nilai 2 ke y
let z = x + y; // Tetapkan nilai x + y ke z:

// Penambahan JavaScript
// Operator Penjumlahan (+) menambahkan angka:
let a = 5;
let b = 2;
let c = a + b;
console.log(c); // output 7

// Perkalian JavaScript
// Operator Perkalian (*) mengalikan angka:
let d = 5;
let e = 2;
let f = d * e;
console.log(f); // output 10


